package javacodes.localhost;

import java.io.*;

public class HTMLReader {
    public static void main(String[] args) {
        try {
            FileReader reader = new FileReader("abc.html");
            int character;
            while ((character = reader.read()) != -1) {
                System.out.print((char) character);
            }
            reader.close();
        } catch (IOException e) {
            e.printStackTrace();
        }
    }
}
